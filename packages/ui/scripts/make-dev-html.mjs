/* Generate packages/ui/index.html as a dev-mode entry for Vite's dev
 * server. Reads the production packages/ui/static/index.html and swaps
 * the script tag to load /src/app.ts as an ES module so Vite's HMR
 * pipeline takes over.
 *
 * Run before `vite` (dev-server mode); output is gitignored.
 */

import { existsSync, readFileSync, writeFileSync } from "node:fs";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const scriptDir = dirname(fileURLToPath(import.meta.url));
const uiRoot = resolve(scriptDir, "..");
const source = resolve(uiRoot, "static", "index.html");
const dest = resolve(uiRoot, "index.html");

if (!existsSync(source)) {
	console.error(`make-dev-html: source missing: ${source}`);
	process.exit(1);
}

const PROD_SCRIPT = /<script\s+src="\/assets\/app\.js"\s*><\/script>/;
const DEV_SCRIPT = '<script type="module" src="/src/app.ts"></script>';
const DEV_MARKER = "<!-- DEV HTML — generated by scripts/make-dev-html.mjs -->\n";

const html = readFileSync(source, "utf8");
if (!PROD_SCRIPT.test(html)) {
	console.error("make-dev-html: production script tag not found in static/index.html");
	process.exit(1);
}

const rewritten = DEV_MARKER + html.replace(PROD_SCRIPT, DEV_SCRIPT);
writeFileSync(dest, rewritten, "utf8");
console.log(`make-dev-html: wrote ${dest}`);
